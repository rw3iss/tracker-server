#!/usr/bin/env node
/**
 * Stamp a build-time `version.js` into dist/dashboard/js/.
 *
 * The dashboard reads `window.__TRACKER_DASHBOARD_VERSION_INFO__` at
 * boot to detect "I'm running a different build than the one I last
 * loaded" — when it changes, the auto-update flow clears app storage,
 * shows a changelog modal, and reloads. So this file is the single
 * source of truth for "which build is this".
 *
 * Run inside `pnpm dashboard:bundle` after the static cp from src/
 * dashboard → dist/dashboard. Overwrites the placeholder shipped in
 * source so dev runs (no build) still load something sensible.
 */
import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync, existsSync } from 'node:fs';
import path from 'node:path';

const root = path.resolve(new URL('..', import.meta.url).pathname);
const outDir = path.join(root, 'dist', 'dashboard', 'js');
const outFile = path.join(outDir, 'version.js');

function git(cmd, fallback = '') {
    try { return execSync(`git ${cmd}`, { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim(); }
    catch { return fallback; }
}

const sha       = git('rev-parse --short HEAD', 'unknown');
const fullSha   = git('rev-parse HEAD', 'unknown');
const builtAt   = new Date().toISOString();
// `version` ties identity to the commit + the build moment. Two builds
// of the same commit will still differ by the timestamp suffix — that's
// intentional, it lets the auto-updater detect a redeploy of the same
// SHA (e.g. an env change).
const version   = `${sha}-${Date.now()}`;

// Last 80 commits as structured records. The dashboard renders one row
// per commit (hash → date → subject), so we need each field separately.
// `\x1f` (ASCII unit-separator) keeps subjects safe from collisions
// with any printable separator.
const rawLog = git('log -n 80 --pretty=format:%h%x1f%aI%x1f%s', '');
const commits = rawLog
    .split('\n')
    .filter(Boolean)
    .map(line => {
        const [shortHash, date, ...rest] = line.split('\x1f');
        return { shortHash, date, subject: rest.join('\x1f') };
    });

// Repo URL for "click the hash to view the commit on GitHub". Normalize
// whatever form `git remote get-url` returns (SSH, HTTPS-with-.git,
// HTTPS-without) into a clean `https://host/owner/repo` so the modal
// can append `/commit/<sha>` and have a working link.
const remoteUrl = git('remote get-url origin', '');
const repoUrl = remoteUrl
    .replace(/^git@([^:]+):/,    'https://$1/')   // git@github.com:foo → https://github.com/foo
    .replace(/^ssh:\/\/git@/,    'https://')       // ssh://git@host/foo → https://host/foo
    .replace(/\.git$/,           '')               // strip trailing .git
    .replace(/\/+$/,             '');              // strip trailing slashes

// Legacy joined-string changelog kept for any consumer that reads the
// old field. New consumers should use `commits[]` + `repoUrl`.
const log = commits.map(c => `${c.shortHash} ${c.subject}`).join('\n');

const banner = `// Auto-generated by bin/generate-dashboard-version.mjs at build time.
// Do not edit. The placeholder in src/dashboard/js/version.js exists
// only so dev runs without a build still load something.`;

const out = `${banner}
window.__TRACKER_DASHBOARD_VERSION_INFO__ = {
  version:   ${JSON.stringify(version)},
  sha:       ${JSON.stringify(fullSha)},
  builtAt:   ${JSON.stringify(builtAt)},
  repoUrl:   ${JSON.stringify(repoUrl)},
  commits:   ${JSON.stringify(commits)},
  changelog: ${JSON.stringify(log)},
};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, out, 'utf8');

if (!existsSync(outFile)) {
    console.error(`[generate-dashboard-version] FAILED to write ${outFile}`);
    process.exit(1);
}
console.log(`[generate-dashboard-version] ${version} → ${outFile} (${log.split('\n').length} commits)`);
